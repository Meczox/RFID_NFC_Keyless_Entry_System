/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_I2C1_Init();
  MX_TIM1_Init();
  MX_ADC2_Init();
  MX_ADC3_Init();
  /* USER CODE BEGIN 2 */
  if (PN532_Begin()) {
	  uint8_t msg[] = "PN532 Connected Successfully!\r\n";
	  HAL_UART_Transmit(&huart2, msg, sizeof(msg) - 1, HAL_MAX_DELAY);
  } else {
	  // If it fails, trap the code here forever and continuously transmit the error.
	  uint8_t err_msg[] = "ERROR: NFC Not Connected or Wiring Issue!\r\n";
	  while (1) {
		  HAL_UART_Transmit(&huart2, err_msg, sizeof(err_msg) - 1, HAL_MAX_DELAY);
		  HAL_Delay(500);
	  }
  }
	Door_Init();
	Signalling_Init();
	ManagementConfig_Init();
	AdminMenu_Init();
	coast_lcd_init();
	Schedule_Init();
	if (Clock_Init()) {
		ClockTime_t now;
		char rtcMessage[24];
		if (Clock_GetTime(&now)) {
			int length = snprintf(rtcMessage, sizeof(rtcMessage),
					"RTC %02u:%02u:%02u\r\n", now.hour, now.minute, now.second);
			HAL_UART_Transmit(&huart2, (uint8_t *)rtcMessage, (uint16_t)length, HAL_MAX_DELAY);
		}
	} else {
		uint8_t rtcError[] = "ERROR: RTC initialization failed!\r\n";
		HAL_UART_Transmit(&huart2, rtcError, sizeof(rtcError) - 1U, HAL_MAX_DELAY);
	}

	// WATCHDOG INTERRUPTS
	HAL_ADC_Start(&hadc2);
	HAL_ADC_Start(&hadc3);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	  Handle_NFC_Entry();
	  Handle_Exit();
	  Handle_Admin_Menu();
	  Schedule_Update();
	  Signalling_RunTask();
  }
  /* USER CODE END 3 */
}

